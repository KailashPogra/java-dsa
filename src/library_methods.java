public class library_methods {
    public static void main(String[] args) {
        // math library is not need to be implement it is already implemented
        // math library return double value
        // sqrt is used to find squire root
        System.out.println(Math.sqrt(16));
        System.out.println(Math.sqrt(24));

        // Math.floor  is used to find corresponding lowest value
        System.out.println(Math.floor(5.5));
        System.out.println(Math.floor(5.8));

        // Math.ceil  is used to find corresponding highest value
        System.out.println(Math.ceil(5.8));
        System.out.println(Math.ceil(5.3));
    }
}
